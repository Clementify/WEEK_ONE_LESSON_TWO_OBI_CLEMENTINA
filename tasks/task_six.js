// ● Write a compound condition using logical operators to check if the value is
// between 5 and 10 (inclusive).
// ● If the condition is true, log a message stating that the value is in the
// specified range; otherwise, log a message stating that it is outside the
// range

let num = 6

if (num >= 5 && num <= 10) {
  console.log('The number is in the specified range of 5 and 10')
} else {
  console.log('The number is outside the range')
}