//   Use the "switch" statement
// ● Declare a variable and assign it a string value representing a day of the
// week.
// ● Write a "switch" statement to perform different actions based on the day.
// ● Log a message with a specific action for each day (e.g., "Monday: Start of
// the week"

let day = "days of the week";
let result;

switch (day) {
    case "sunday":
        result = "first day";
        break;
    case "monday":
        result = "second day";
        break;
    case "tuesday":
        result = "second day";
        break;
    case "wednesday":
        result = "third day";
        break;
    case "thursday":
        result = "fourth day";
        break;
    case "friday":
        result = "fifth day";
        break;
    default:
        result = "days of the week";
}
console.log(result);



